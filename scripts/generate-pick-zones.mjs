import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const expansionNames = {
  1: "Classic",
  2: "Kunark",
  3: "Velious",
  4: "Luclin",
  5: "Planes of Power",
  6: "Legacy of Ykesha",
};

const inputPath = resolve(process.argv[2] ?? "data/ZoneNames.txt");
const outputPath = resolve(process.argv[3] ?? "data/pick-zones.ts");
const raw = readFileSync(inputPath, "utf8");

const zones = [];
let skippedMalformed = 0;
let skippedNoPickMin = 0;
let skippedInvalidExpansionRange = 0;
let includedEarly = 0;
let includedAllOther = 0;

for (const [lineIndex, line] of raw.split(/\r?\n/).entries()) {
  const trimmed = line.trim();
  if (!trimmed) continue;

  const fields = trimmed.split("^");
  if (fields.length < 6) {
    skippedMalformed += 1;
    continue;
  }

  const [zoneIdRaw, zoneNameRaw, pickMinRaw, pickMaxRaw, expansionStartRaw, expansionEndRaw] = fields;
  const zoneId = Number.parseInt(zoneIdRaw, 10);
  const zoneName = zoneNameRaw.trim();
  const pickMin = Number.parseInt(pickMinRaw, 10);
  const pickMax = Number.parseInt(pickMaxRaw, 10);
  const expansionStart = Number.parseInt(expansionStartRaw, 10);
  const expansionEnd = Number.parseInt(expansionEndRaw, 10);

  if (
    !Number.isInteger(zoneId)
    || !zoneName
    || !Number.isInteger(pickMin)
    || !Number.isInteger(pickMax)
    || !Number.isInteger(expansionStart)
    || !Number.isInteger(expansionEnd)
  ) {
    skippedMalformed += 1;
    continue;
  }

  if (pickMin <= 0) {
    skippedNoPickMin += 1;
    continue;
  }

  if (expansionStart < 1 || expansionEnd < 1 || expansionEnd < expansionStart) {
    skippedInvalidExpansionRange += 1;
    continue;
  }

  const isEarlyTlpZone = expansionStart <= 6 && expansionEnd <= 6;
  const expansionIds = isEarlyTlpZone
    ? Array.from(
      { length: expansionEnd - expansionStart + 1 },
      (_, index) => expansionStart + index,
    )
    : [];
  const expansionName = isEarlyTlpZone
    ? expansionStart === expansionEnd
      ? expansionNames[expansionStart]
      : `${expansionNames[expansionStart]} - ${expansionNames[expansionEnd]}`
    : "All Other Zones";
  if (isEarlyTlpZone) includedEarly += 1;
  else includedAllOther += 1;

  zones.push({
    zoneId,
    zoneName,
    pickMin,
    pickMax,
    expansionStart,
    expansionEnd,
    expansionIds,
    expansionName,
    section: isEarlyTlpZone ? "early-tlp" : "all-other",
    sourceLine: lineIndex + 1,
  });
}

zones.sort((a, b) =>
  (a.section === b.section ? 0 : a.section === "early-tlp" ? -1 : 1)
  || a.expansionStart - b.expansionStart
  || a.expansionEnd - b.expansionEnd
  || a.zoneName.localeCompare(b.zoneName)
  || a.zoneId - b.zoneId,
);

const output = `// Generated by scripts/generate-pick-zones.mjs from data/ZoneNames.txt.
// Do not edit by hand; update the source text file and regenerate.

export type PickZone = {
  zoneId: number;
  zoneName: string;
  pickMin: number;
  pickMax: number;
  expansionStart: number;
  expansionEnd: number;
  expansionIds: readonly number[];
  expansionName: string;
  section: "early-tlp" | "all-other";
  sourceLine: number;
};

export const pickExpansionNames = ${JSON.stringify(expansionNames, null, 2)} as const;

export const pickZones = ${JSON.stringify(zones, null, 2)} as const satisfies PickZone[];

export const pickZoneGenerationStats = ${JSON.stringify({
  source: "data/ZoneNames.txt",
  included: zones.length,
  includedEarly,
  includedAllOther,
  skippedMalformed,
  skippedNoPickMin,
  skippedInvalidExpansionRange,
}, null, 2)} as const;
`;

writeFileSync(outputPath, output);
console.log(`Generated ${zones.length} pick zones at ${outputPath}`);
